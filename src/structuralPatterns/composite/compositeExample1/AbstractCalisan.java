package structuralPatterns.composite.compositeExample1;

public abstract class AbstractCalisan implements Calisan{
    protected int id;
    protected String isim;

    public AbstractCalisan(int id, String isim) {
        this.id = id;
        this.isim = isim;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getIsim() {
        return isim;
    }

    public void setIsim(String isim) {
        this.isim = isim;
    }
}
